                    Copy, cut, delete, select or comment:
                    <ul>
                        <li><i>N</i> lines above/below the current line</li>
                        <li>
                            A range of lines relative to the current line (e.g., comment lines 5 through 10 above the 
                            current line)
                        </li>
                        <li>
                            A range of lines in both directions relative to the current line (e.g., comment 5 lines 
                            above and 10 lines below the current line)
                        </li>
                    </ul>
